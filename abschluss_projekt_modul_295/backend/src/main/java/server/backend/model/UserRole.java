package backend.model;

/**
 * Diese Enum repräsentiert die verschiedenen Rollen, die ein Benutzer im System haben kann.
 */
public enum UserRole {
    /**
     * Administratorrolle mit erweiterten Rechten.
     */
    ADMIN,
     /**
     * Standardbenutzerrolle mit eingeschränkten Rechten.
     */
    USER
}